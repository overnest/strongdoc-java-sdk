package com.strongsalt.strongdoc.sdk.api.responses;


/**
 * This class holds information for a single document.
 */
public class DocumentInfo {
    /**
     * The document ID
     */
    private String docID;
    /**
     * The document name
     */
    private String docName;
    /**
     * The document size
     */
    private long size;

    /**
     * Constructs a document info
     *
     * @param docID   The document ID
     * @param docName The document name
     * @param size    The document size
     */
    public DocumentInfo(final String docID, final String docName, final long size) {
        this.docID = docID;
        this.docName = docName;
        this.size = size;
    }

    /**
     * Returns the document ID
     *
     * @return The document ID
     */
    public String getDocID() {
        return docID;
    }

    /**
     * Returns the document name
     *
     * @return The document name
     */
    public String getDocName() {
        return docName;
    }

    /**
     * Returns the document size
     *
     * @return The document size
     */
    public long getSize() {
        return size;
    }
}
